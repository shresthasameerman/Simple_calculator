def add(x,y):
    return x+y
def substract(x,y):
    return x-y
def multiply(x,y):
    return x*y
def divide(x,y):
    return x/y
def truncated_divide(x,y):
    return x//y
def modulus(x,y):
    return x%y
def exponentiate(x,y):
    return x**y
addition = add(2.5,3.6)
substraction = substract(10.21,2.90)
multiplication = multiply(3.1,6.4)
division=divide(12.0,4.0)
truncated_division=truncated_divide(15,2)
modulus=modulus(13,5)
exponentiation=exponentiate(2,8)
print(f"Addition: {addition}")
print(f"Subtraction: {substraction}")
print(f"Multiplication: {multiplication}")
print(f"Division: {division}")
print(f"Truncated Division: {truncated_division}")
print(f"Modulus: {modulus}")
print(f"Exponentiation: {exponentiation}")
